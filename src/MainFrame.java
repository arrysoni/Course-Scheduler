        ArrayList<StudentEntry> student = StudentQueries.getAllStudents();
        String studentID = student.get(ScheduleClassSelectStudentjComboBox.getSelectedIndex()).getStudentID();
        Timestamp timestamp = new Timestamp(System.currentTimeMillis());
        String status;
        int seats = ClassQueries.getClassSeats(semester,courseCode);
