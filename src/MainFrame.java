    private void dropStudentSubmitjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropStudentSubmitjButtonActionPerformed
        // TODO add your handling code here:
        // Drops student from the educational institution at once, this affects the waitlisted students if they were on the waitlist for a class, and others likewise.
        dropStudentjTextArea.setText("");
        int selectedStudentIndex = (int)dropStuChooseStujComboBox.getSelectedIndex();
        
        ArrayList<StudentEntry> students = StudentQueries.getAllStudents();
        StudentEntry selectedStudent = (StudentEntry) dropStuChooseStujComboBox.getSelectedItem();
        dropStudentjTextArea.append(selectedStudent.getLastName() + "," + selectedStudent.getFirstName()+" "+selectedStudent.getStudentID() + " has been dropped from the list of students.\n\n");
        
        // This part of the code handles the part where a student is dropped from the student list and affects the scheduled classes for all the others students accordingly.
        
        for (String currentSemester : SemesterQueries.getSemesterList())
        {
            System.out.println("");
            dropStudentjTextArea.append("For Semester: " + currentSemester + "\n");
            System.out.print(students.get(selectedStudentIndex).getStudentID());
            String droppedCourses = "";
            String scheduledCourses = "";
           
            for (ScheduleEntry entry : ScheduleQueries.getScheduleByStudent(currentSemester, students.get(selectedStudentIndex).getStudentID()))
            {
                ScheduleQueries.dropStudentScheduleByCourse(currentSemester, students.get(selectedStudentIndex).getStudentID(), entry.getCourseCode());
                StudentEntry selectedStudentD = (StudentEntry) dropStuChooseStujComboBox.getSelectedItem();
                
                // check if the student was in the waitlist
                if (entry.getStatus().equals("W"))
                {
                    droppedCourses += selectedStudentD.getLastName() + ", " + selectedStudentD.getFirstName()+ " " +  selectedStudentD.getStudentID() + "has been dropped from the waitlist for " + entry.getCourseCode() + "\n";
                }
                else
                {
                    droppedCourses += selectedStudentD.getLastName() + ", " + selectedStudentD.getFirstName()+ " " +  selectedStudentD.getStudentID()  + " has been dropped from " + entry.getCourseCode() + "\n";

                }
                
                ArrayList<ScheduleEntry> waitlistedStudents = ScheduleQueries.getWaitlistedStudentsByClass(currentSemester, entry.getCourseCode());
                
                if (waitlistedStudents.size() > 0) 
                {
                    ScheduleQueries.updateScheduleEntry(waitlistedStudents.get(0));
                    StudentEntry shiftStudent = StudentQueries.getStudent(waitlistedStudents.get(0).getStudentID());
                    scheduledCourses += shiftStudent.getLastName() + ", " + shiftStudent.getFirstName() + " " + waitlistedStudents.get(0).getStudentID() + " has been scheduled into " + waitlistedStudents.get(0).getCourseCode() + "\n";
                }

            }
            
            if (!droppedCourses.isEmpty() || !scheduledCourses.isEmpty())
                {
                    
                    dropStudentjTextArea.append(droppedCourses);
                    dropStudentjTextArea.append(scheduledCourses);
            }
        }
        
        
        System.out.println("");
        dropStudentjTextArea.append(selectedStudent.getLastName() + "," + selectedStudent.getFirstName()+" "+selectedStudent.getStudentID() + " has been removed from all the student combo boxes.\n\n");
        
        
        StudentQueries.dropStudent(students.get(selectedStudentIndex).getStudentID());
        rebuildStudentComboBoxes();
       
        
              
        
    }//GEN-LAST:event_dropStudentSubmitjButtonActionPerformed

