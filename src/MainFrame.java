    private void dropClassSubmitjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropClassSubmitjButtonActionPerformed
        // TODO add your handling code here:
        // Drops a class from the directory. 
        String selectedClass = (String)dropClassChooseClassjComboBox.getSelectedItem();
        String currentSemester = currentSemesterLabel.getText();
        
        ArrayList<StudentEntry> scheduledStudent = MultiTableQueries.getScheduledStudentsByClass(currentSemester,selectedClass);
        ArrayList<StudentEntry> waitlistedStudent = MultiTableQueries.getWaitlistedStudentsByClass(currentSemester,selectedClass);
        
        
        // This part of the code drops the course entirely and removes all the scheduled and waitlisted students from the class enrollment.
        dropClassjTextArea.append("Scheduled students dropped from course \n");
        for (StudentEntry student: scheduledStudent)
        {
            dropClassjTextArea.append(student.getLastName() + ", " + student.getFirstName() + " " + student.getStudentID() + '\n');
        }
        
        dropClassjTextArea.append("Waitlisted students dropped from course \n");
        if (waitlistedStudent.size() > 0)
        {
            for (StudentEntry student: waitlistedStudent)
            {
                dropClassjTextArea.append(student.getLastName() + ", " + student.getFirstName() + " " + student.getStudentID() + '\n');
            }
        }

        ClassQueries.dropClass(currentSemester, selectedClass);
        ScheduleQueries.dropScheduleByCourse(currentSemester, selectedClass);
        rebuildCourseComboBoxes();
        
        
    }//GEN-LAST:event_dropClassSubmitjButtonActionPerformed

